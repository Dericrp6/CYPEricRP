print("DISTANCIA_ENTRE_DOS_PUNTOS")

X1 = float(input("Dame el numero de X1: "))
Y1 = float(input("Dame el numero de Y1: "))
X2 = float(input("Dame el numero de X2: "))
Y2 = float(input("Dame el numero de Y2: "))

DIS = ((X1 - X2) **2 + (Y1 - Y2) **2) **0.5

print(f"La distancia entre estos dos puntos es de {DIS} ")

